let clubsRender = document.querySelector('#clubs')
renderClubs = (clubs) => {
    var countClub = 0;
    clubs.forEach(e => {
        var clubList = [];
        var elink;
        e.list.forEach(el => {
            elink = `<span style="margin-left:5px"><a href="${el.link}"target="_blank">${el.name}</a></span>`
            clubList.push(elink)
        })
        let prod = `
        <!-- ${e.clubName} -->
            <div class="blog-content ml-15">
                <p>${++countClub}. <b>${e.clubName}:</b> ${clubList}</p>
            </div>
    
`
        clubsRender.insertAdjacentHTML("beforeend", prod)
    })
}
renderClubs(clubs)